const arabicIndicMap: Record<string, string> = {
  '0': '٠',
  '1': '١',
  '2': '٢',
  '3': '٣',
  '4': '٤',
  '5': '٥',
  '6': '٦',
  '7': '٧',
  '8': '٨',
  '9': '٩',
};

export function toArabicIndic(value: string | number): string {
  return String(value).replace(/[0-9]/g, (digit) => arabicIndicMap[digit]);
}
